DELIMITER // 
CREATE PROCEDURE spFetchProjects()
BEGIN
	SELECT WORK.WORK_ID, WORK.WORK_NAME, WORK_IMAGES.IMAGE_NAME, WORK.WORK_DESCRIPTION, WORK.WORK_COVER_IMAGE, CATEGORY.CATEGORY_DESCRIPTION
    FROM WORK 
    INNER JOIN WORK_IMAGES ON WORK.WORK_ID = WORK_IMAGES.WORK_ID
    INNER JOIN CATEGORY ON WORK.CATEGORY_ID = CATEGORY.CATEGORY_ID;
END //
DELIMITER ;